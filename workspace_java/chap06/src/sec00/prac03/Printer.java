package sec00.prac03;

public class Printer {
	int println(int a) {
		System.out.println(a);
		return a;
	}
	boolean println(boolean a) {
		System.out.println(a);
		return a;
	}
	double println(double a) {
		System.out.println(a);
		return a;
	}
	String println(String a) {
		System.out.println(a);
		return a;
	}
}
